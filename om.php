      <p>RaceRunning er en liten idrett i Norge. Og for øyeblikket er det 14 aktive RaceRunere i Steinkjer, det er også ei i Vestfold og ei i Trondheim (2017).
      Sporten er stor i hjemlandet Danmark, Sverige, Scotland osv., men har i mang år vært liten i Norge. I 2013 steg antallet norske utøvere fra 1 til 3. I 2014 ble det satt i gang et prosjekt for å få sporten opp å gå i Steinkjer. Prosjektet fikk inn 350.000kr fra gjensidigestiftelsen. Noen av disse pengene ble brukt til å kjøp inn sykler fra Danmark. Nå trene 14 utøvere hver mandag på Steinkjer.
