      <p>RaceRunning foregår på vanlig friidrettsbaner, hvor man konkurrerer i distanser fra 40meter opp til maraton.
        Konkurrentene blir delt inn i grupper etter funksjonshemning, RR1-3 er gruppen for utøvere med forskjellig grad av cp. RR4 er gruppen for andre diagnoser og RR5 er for utøvere som ikke styrer sykkelen selv.
        Man deltar med en trehjuls sykkel hvor man sitter på et sykkelsete, og løper med bena. Dette er enten en Race Runner, eller en Petra. (For å se mer om petra sykkel, gå inn på i <a href="http://by-conniehansen.com">By Conniehansen.com!</a>) En Race Runner er en spesialbygd sykkel uten pedaler, som gjør at balanse ikke er et problem.
      </p> <br/>
      <p>RaceRunning ble oppfunnet i Danmark, og den første RaceRunneren ble konstruert i 1989.
        Fordi balanse ikke er problem, åpner RaceRunning muligheten til å inkludere mennesker med cerebral parese, leddgikt og amputerte.
        Mansoor Siddiqi er en dansk mann som pleide å delta i friidrett. Han måtte løpe baklengs pga cp, og brukte manuell rullestol som han dyttet. Connie Hansen, ei paraolympier, lagde petra så han kunne løpe mer oppreist og riktig vei.
        RaceRunning ble mer og mer populært.
        Det går an å delta både på mosjons - å elitenivå. De som vil, kan i dag konkurrere, men petra gir også handicappede, som kanskje ikke har annen mulighet til å komme opp å røre på seig, mulighet til å trene utholdenhet, hurtighet, spenst mm.
        I dag arrangeres det mindre stevna for RaceRunning i verden. Men også internasjonale stevna, em, vm osv. RaceRunning c&c er nok det største arrangementet, med både leir å konkurranse. Det jobbes aktivt med å få RaceRunning som en grein i Paralympics.
        Gutter og jenter konkurrerer ikke mot hverandre, og de forskjellige RR klassene konkurrerer kun mot de de er i samme RR klasse med.
        Det er satt verdensrekorder i <a href="http://www.racerunning.org/document/default.asp?documentID=265&id=155"> RaceRunning</a>.
